package com.vjtechsolution.nex;

public class ListDetailVisit {
    String nama_toko, kode_asset, jarak_scan, tgl_visit;
    int no;

    public String getNama_toko() {
        return nama_toko;
    }

    public void setNama_toko(String nama_toko) {
        this.nama_toko = nama_toko;
    }

    public String getKode_asset() {
        return kode_asset;
    }

    public void setKode_asset(String kode_asset) {
        this.kode_asset = kode_asset;
    }

    public String getJarak_scan() {
        return jarak_scan;
    }

    public void setJarak_scan(String jarak_scan) {
        this.jarak_scan = jarak_scan;
    }

    public String getTgl_visit() {
        return tgl_visit;
    }

    public void setTgl_visit(String tgl_visit) {
        this.tgl_visit = tgl_visit;
    }

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }
}
